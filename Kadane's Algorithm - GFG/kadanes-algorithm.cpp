class Solution{
    public:
    // arr: input array
    // n: size of array
    //Function to find the sum of contiguous subarray with maximum sum.
    long long maxSubarraySum(int arr[], int n){
        
        // Your code here
       
        int ls=arr[0]; //largest sum
        int cs=arr[0]; // current sum
        for(long long i=1;i<n;i++){
            cs=max(arr[i],cs+arr[i]); // to handle case when all array elements are negative.
      ls=max(ls,cs);
        }
      return ls;
        }
};

